            sql = "INSERT INTO tb_spp (biaya, bulan, tahun, status, nisn) VALUES ('"+strSPP+"', '"+strBulan+"', '"+intTahun+"', 'Belum Bayar', '"+StaffTUSiswa.strNisn+"')";
