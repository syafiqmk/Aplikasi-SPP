    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        
        // Declare variabel
        String strId = txtTId.getText();
        String strStatus = txtTStatus.getText();
        String strTanggal = LocalDate.now().toString();
        
        if(strStatus.equals("Belum Bayar")) {
            try {
                sql = "INSERT INTO tb_transaksi (tanggal, id_spp, id_staff_tu) VALUES('"+strTanggal+"', '"+strId+"', '"+DBConnection.id+"')";
                stmt.execute(sql);
                JOptionPane.showMessageDialog(null, "SPP berhasil dibayar!");
                getData();
                clearTInput();
            } catch(Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            
            try {
                sql = "UPDATE tb_spp SET status = 'Sudah Bayar' WHERE id = '"+strId+"'";
                stmt.execute(sql);
                getData();
                clearTInput();
            } catch(Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "SPP Sudah Dibayar!");
        }
        
    }//GEN-LAST:event_btnBayarActionPerformed

    private void btnDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailActionPerformed
        // TODO add your handling code here:
        
        // Declare variable
        String strId = txtTId.getText();
        String strStatus = txtTStatus.getText();
        
        if(strStatus.equals("Sudah Bayar")) {
            try {
                sql = "SELECT * FROM tb_transaksi s JOIN tb_staff_tu tu WHERE s.id_spp = '"+strId+"' AND s.id_staff_tu = tu.id";
                rs = stmt.executeQuery(sql);
                if(rs.next()) {
                    JOptionPane.showMessageDialog(null, "Tanggal : " + rs.getString("tanggal") + "\nNama : " + rs.getString("nama"));
                }
            } catch(Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "SPP Belum Dibayar!");
        }
    }//GEN-LAST:event_btnDetailActionPerformed


