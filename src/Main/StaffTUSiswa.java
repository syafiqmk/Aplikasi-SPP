    private void btnNisnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNisnActionPerformed
        // TODO add your handling code here:
        
        // Declare variabe and get user input
        String strNisn = txtNisn.getText();
        
        // Search by nisn process
        try {
            sql = "SELECT s.nisn, s.nama, k.tingkat, k.kelas FROM tb_siswa s JOIN tb_Kelas k WHERE s.id_kelas = k.id AND s.nisn LIKE '%"+strNisn+"%' ORDER BY k.tingkat ASC, k.kelas ASC";
            rs = stmt.executeQuery(sql);
            tblSiswa.setModel(DbUtils.resultSetToTableModel(rs));
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnNisnActionPerformed

    private void btnKelasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKelasActionPerformed
        // TODO add your handling code here:
        
        // Declare variable and get user input
        String kelas = cmbKelas.getSelectedItem().toString();
        String[] id = kelas.split("-");
        
        // Search by kelas process
        try {
            sql = "SELECT s.nisn, s.nama, k.tingkat, k.kelas FROM tb_siswa s JOIN tb_Kelas k WHERE s.id_kelas = k.id AND s.id_kelas = '"+id[0]+"' ORDER BY k.tingkat ASC, k.kelas ASC";
            rs = stmt.executeQuery(sql);
            tblSiswa.setModel(DbUtils.resultSetToTableModel(rs));
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnKelasActionPerformed

