    private void getData() {
        try {
            sql = "SELECT s.nisn, s.nama, k.tingkat, k.kelas FROM tb_siswa s JOIN tb_Kelas k WHERE s.id_kelas = k.id ORDER BY k.tingkat ASC, k.kelas ASC";
            rs = stmt.executeQuery(sql);
            tblSiswa.setModel(DbUtils.resultSetToTableModel(rs));
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void getKelas() {
        try {
            sql = "SELECT * FROM tb_kelas ORDER BY tingkat ASC, kelas ASC";
            rs = stmt.executeQuery(sql);
            while(rs.next()) {
                cmbKelas.addItem(rs.getString("id") + "-" + rs.getString("tingkat") + rs.getString("kelas"));
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

